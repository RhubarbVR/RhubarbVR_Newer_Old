namespace OpusDotNet
{
	/// <summary>
	/// Specifies the bandpasses.
	/// </summary>
	public enum Bandwidth
	{
		/// <summary>
		/// 4 kHz passband.
		/// </summary>
		NarrowBand = 1101,
		/// <summary>
		/// 6 kHz passband.
		/// </summary>
		MediumBand = 1102,
		/// <summary>
		/// 8 kHz passband.
		/// </summary>
		WideBand = 1103,
		/// <summary>
		/// 12 kHz passband.
		/// </summary>
		SuperWideBand = 1104,
		/// <summary>
		/// 20 kHz passband.
		/// </summary>
		FullBand = 1105
	}
}
